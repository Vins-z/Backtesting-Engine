MarketMetadata get_market_metadata(const std::string& symbol) {
    auto it = MARKET_METADATA.find(symbol);
    if (it != MARKET_METADATA.end()) {
        return it->second;
    }
    
    // Return default metadata for unknown symbols
    return {symbol, symbol, "Unknown", "Unknown"};
}

std::vector<std::string> get_market_symbols(const std::string& market) {
    if (market == "sp500") {
        return SP500_TOP_50;
    } else if (market == "nifty50") {
        return NIFTY_50;
    } else if (market == "bse" || market == "sensex") {
        return BSE_SENSEX_50;
    }
    
    return {};
}

std::vector<std::string> get_all_known_symbols() {
    std::vector<std::string> symbols;
    symbols.reserve(MARKET_METADATA.size());
    
    for (const auto& [symbol, metadata] : MARKET_METADATA) {
        symbols.push_back(symbol);
    }
    
    return symbols;
}

